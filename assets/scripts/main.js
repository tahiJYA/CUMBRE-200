let slideIndex = 0;

function showSlides() {
  const slides = document.querySelectorAll('.card');
  const slidesPerPage = window.innerWidth < 700 ? 2 : 4; // Mostrar 2 tarjetas en dispositivos móviles
  const slidesCount = slides.length;
  const totalPages = Math.ceil(slidesCount / slidesPerPage);

  slideIndex = slideIndex < 0 ? 0 : slideIndex;
  slideIndex = slideIndex >= totalPages ? totalPages - 1 : slideIndex;

  const startIndex = slideIndex * slidesPerPage;
  const endIndex = Math.min(startIndex + slidesPerPage, slidesCount);

  for (let i = 0; i < slidesCount; i++) {
    slides[i].style.display = 'none';
  }

  for (let i = startIndex; i < endIndex; i++) {
    slides[i].style.display = 'block';
  }

  // Actualizar los puntos indicadores
  updateDots(totalPages);
}

function prevSlide() {
  slideIndex--;
  showSlides();
}

function nextSlide() {
  slideIndex++;
  showSlides();
}

function updateDots(totalPages) {
  const dotsContainer = document.getElementById('dots-container');
  dotsContainer.innerHTML = '';

  for (let i = 0; i < totalPages; i++) {
    const dot = document.createElement('span');
    dot.classList.add('dot');
    if (i === slideIndex) {
      dot.classList.add('active-dot');
    }
    dot.setAttribute('onclick', `goToSlide(${i})`);
    dotsContainer.appendChild(dot);
  }
}

function goToSlide(index) {
  slideIndex = index;
  showSlides();
}

document.addEventListener('DOMContentLoaded', showSlides);
window.addEventListener('resize', showSlides);


